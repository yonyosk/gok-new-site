const LangCtx = createCtx(null);
function LangProvider({ children }) {
  const [lang, setLang] = useLangState(() => localStorage.getItem("gok-lang") || "he");
  const set = (l) => { localStorage.setItem("gok-lang", l); setLang(l); };
  const t = STR[lang];
  const dir = t.dir;
  React.useEffect(() => {
    document.documentElement.lang = lang;
    document.documentElement.dir = dir;
  }, [lang, dir]);
  return <LangCtx.Provider value={{ lang, setLang: set, t, dir }}>{children}</LangCtx.Provider>;
}
const useLang = () => useCtx(LangCtx);

Object.assign(window, { STR, LangProvider, useLang });
